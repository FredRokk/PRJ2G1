GUIThreadFunctor::GUIThreadFunctor(	osapi::MsgQueue* printMq, 
									osapi::MsgQueue* gameMq)
{
	printMq_ = printMq;
	gameMq_ = gameMq;
}

/*////////////////////////////////////////////////////////////
////////////   			Protected Methods		//////////////
*/////////////////////////////////////////////////////////////

void GUIThreadFunctor::run(){
	init();

	PrintThreadFunctor printTF(printMq_, gameMq_);
	osapi::Thread printThread(&printTF);
	printThread.start();

	//a.exec()
	printThread.join();

}

